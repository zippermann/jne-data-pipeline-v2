# ── DYNAMIC GENERATORS ────────────────────────────────────────────────────────

def generate_manifest_config(df_columns: List[str]) -> Dict[str, Dict]:
    """
    Returns per-prefix config dicts for OM, IM, HM, and TM1..TMN based on the
    actual columns present in the data.  Each prefix produces two entries:
    one for the manifest header (CMS_MANIFEST_{PFX}) and one for the manifest
    content rows (CMS_MFCNOTE_{PFX}).
    Index codes for manifest rules reference table H (CMS_MANIFEST) and I (CMS_MFCNOTE).
    """
    result: Dict[str, Dict] = {}
    prefixes: set = set()

    for p in ("om", "im", "hm"):
        if f"{p}_man_no" in df_columns:
            prefixes.add(p)

    tm_pat = re.compile(r"^(tm\d+)_man_no$")
    for col in df_columns:
        m = tm_pat.match(col)
        if m:
            prefixes.add(m.group(1))

    transit_prefixes = sorted(
        (pfx for pfx in prefixes if pfx.startswith("tm")),
        key=lambda pfx: int(pfx[2:]),
    )

    for pfx in sorted(prefixes):
        # ── manifest header ──────────────────────────────────────────────────
        man_mandatory = [
            c for c in [
                f"{pfx}_manifest_no", f"{pfx}_manifest_date", f"{pfx}_manifest_route",
                f"{pfx}_manifest_from", f"{pfx}_manifest_thru", f"{pfx}_manifest_approved",
                f"{pfx}_manifest_origin", f"{pfx}_manifest_code", f"{pfx}_manifest_uid",
                f"{pfx}_manifest_crdate", f"{pfx}_manifest_canceled",
            ] if c in df_columns
        ]
        crdate_col = f"{pfx}_manifest_crdate"
        time_rules: List[Dict[str, str]] = []
        if pfx == "om" and transit_prefixes:
            first_tm_crdate = f"{transit_prefixes[0]}_manifest_crdate"
            if crdate_col in df_columns and first_tm_crdate in df_columns:
                time_rules.append({
                    "start": crdate_col,
                    "end": first_tm_crdate,
                    "assign_to": crdate_col,
                })
        if pfx in transit_prefixes and crdate_col in df_columns:
            tm_pos = transit_prefixes.index(pfx)
            next_col = (
                f"{transit_prefixes[tm_pos + 1]}_manifest_crdate"
                if tm_pos + 1 < len(transit_prefixes)
                else "im_manifest_crdate"
            )
            if next_col in df_columns:
                time_rules.append({
                    "start": crdate_col,
                    "end": next_col,
                    "assign_to": crdate_col,
                })
        if pfx == "im" and crdate_col in df_columns:
            for target_col in (
                "mhocnote_signdate",
                "cost_m_manifest_date",
                "mrsheet_date",
            ):
                if target_col in df_columns:
                    time_rules.append({
                        "start": crdate_col,
                        "end": target_col,
                        "assign_to": crdate_col,
                    })

        result[f"CMS_MANIFEST_{pfx.upper()}"] = {
            "tier": "CMS",
            "primary_key": [f"{pfx}_man_no"],
            "mandatory": man_mandatory,
            "consistency_pairs": [
                (f"{pfx}_manifest_route", "cnote_destination"),  # CONS2H4
            ],
            "regex_rules": {
                f"{pfx}_man_no":            r"^[A-Z]{3}/(OM|TM|IM|HM)/\d+$",
                f"{pfx}_manifest_route":    ID_PATTERNS["ALNUM"],
                f"{pfx}_manifest_from":     ID_PATTERNS["ALNUM"],
                f"{pfx}_manifest_thru":     ID_PATTERNS["ALNUM"],
                f"{pfx}_manifest_origin":   ID_PATTERNS["ALNUM"],
                f"{pfx}_manifest_code":     r"^[1-3]$",
                f"{pfx}_manifest_approved": ID_PATTERNS["YESNO"],
            },
            "datetime_rules": [
                c for c in [f"{pfx}_manifest_date", f"{pfx}_manifest_crdate"]
                if c in df_columns
            ],
            "uniqueness_keys": [],  # manifest header repeats per shipment in flat table
            "timeliness_rules": time_rules,
            "pass_threshold": 85.0,
            "sheet_prefix": f"MANIFEST_{pfx.upper()}",
        }

        # ── manifest content (MFCNOTE) ────────────────────────────────────────
        mfc_mandatory = [
            c for c in [
                f"{pfx}_mfc_no", f"{pfx}_man_no", f"{pfx}_bag_no",
                f"{pfx}_mfc_weight", f"{pfx}_mfc_crdate",
            ] if c in df_columns
        ]
        result[f"CMS_MFCNOTE_{pfx.upper()}"] = {
            "tier": "CMS",
            "primary_key": [f"{pfx}_man_no"],
            "mandatory": mfc_mandatory,
            "consistency_pairs": [
                (f"{pfx}_mfc_weight", "cnote_weight"),  # CONS2I4
            ],
            "regex_rules": {
                f"{pfx}_man_no":     r"^[A-Z]{3}/(OM|TM|IM|HM)/\d+$",
                f"{pfx}_bag_no":     r"^\d+$",
                f"{pfx}_mfc_weight": ID_PATTERNS["NUMERIC"],
            },
            "datetime_rules": [
                c for c in [f"{pfx}_mfc_crdate"] if c in df_columns
            ],
            "uniqueness_keys": [
                [f"{pfx}_man_no"],
            ],
            "timeliness_rules": [],
            "pass_threshold": 85.0,
            "sheet_prefix": f"MFCNOTE_{pfx.upper()}",
        }

    return result


def generate_value_conditionals(df_columns: List[str]) -> List[Tuple]:
    """
    Returns VALUE_CONDITIONAL_COMPLETENESS plus dynamically generated rules for
    each manifest prefix: if {pfx}_manifest_canceled == 'Y' then
    {pfx}_manifest_canceled_uid must be non-empty.
    """
    rules = list(VALUE_CONDITIONAL_COMPLETENESS)
    pat = re.compile(r"^(tm\d+|om|im|hm)_man_no$")
    for col in df_columns:
        m = pat.match(col)
        if m:
            pfx = m.group(1)
            canceled_col = f"{pfx}_manifest_canceled"
            uid_col = f"{pfx}_manifest_canceled_uid"
            if canceled_col in df_columns and uid_col in df_columns:
                rules.append((
                    canceled_col, "Y", uid_col, None,
                    f"{pfx}_canceled_requires_uid",
                ))
    return rules
