def rules_for_table(table: str) -> List[Rule]:
    """Return all structured rules for one table."""
    return [rule for rule in RULE_CATALOG if rule.table == table]


def rules_for_element(element: str) -> List[Rule]:
    """Return all structured rules for one DQ element."""
    return [rule for rule in RULE_CATALOG if rule.element.lower() == element.lower()]


def rules_for_family(rule_family: str) -> List[Rule]:
    """Return all structured rules for one rule family, e.g. VALD10."""
    return [rule for rule in RULE_CATALOG if rule.rule_family == rule_family]


def rule_summary() -> Dict[str, int]:
    """Return counts by DQ element for quick sanity checks."""
    summary = {element: 0 for element in DQ_ELEMENTS}
    for rule in RULE_CATALOG:
        summary[rule.element] = summary.get(rule.element, 0) + 1
    return summary
