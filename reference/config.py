@dataclass(frozen=True)
class Rule:
    """One human-readable governance rule."""

    index_code: Optional[str]
    element: str
    rule_family: str
    table: str
    columns: Tuple[str, ...]
    description: str
    implementation: str
    regex_pattern: Optional[str] = None
    assign_to: Optional[str] = None
    condition_label: Optional[str] = None


