def _label_from_parentheses(text: str) -> Optional[str]:
    """Extract the final parenthesized label from implementation text."""
    matches = re.findall(r"\(([^()]*)\)", text)
    return matches[-1] if matches else None


def _regex_from_description(text: str) -> Optional[str]:
    """Extract the regex pattern from a validity rule description."""
    marker = "pattern: "
    if marker not in text:
        return None
    return text.split(marker, 1)[1].rstrip(".")


def _assign_to_from_implementation(text: str) -> Optional[str]:
    """Extract the timeliness output label from implementation text."""
    marker = "assigned to "
    if marker not in text:
        return None
    return text.split(marker, 1)[1].strip()


def _required_value_from_description(text: str) -> Optional[str]:
    """Extract a required value from descriptions such as 'field = Y'."""
    match = re.search(r"=\s*([A-Za-z0-9_/-]+)", text)
    return match.group(1).upper() if match else None


def _with_execution_metadata(rule: Rule) -> Rule:
    """
    Fill optional execution fields that make RULE_CATALOG the runtime source.

    The visible rule block remains the review-facing truth.  These fields are
    derived from the rule's own description/implementation text so downstream
    dictionaries can be generated from RULE_CATALOG instead of maintained as a
    second catalog.
    """
    data = rule.__dict__.copy()

    if rule.element == "Validity" and rule.rule_family != "VALD4":
        data["regex_pattern"] = rule.regex_pattern or _regex_from_description(
            rule.description
        )

    if rule.element == "Timeliness" and rule.rule_family == "TIME1":
        data["assign_to"] = rule.assign_to or _assign_to_from_implementation(
            rule.implementation
        )

    if rule.element == "Completeness" and rule.rule_family == "COMP2":
        data["condition_label"] = rule.condition_label or _label_from_parentheses(
            rule.implementation
        )

    return Rule(**data)


RULE_CATALOG = [_with_execution_metadata(rule) for rule in RULE_CATALOG]


def _completeness_fields_from_catalog() -> Dict[str, List[str]]:
    fields: Dict[str, List[str]] = {}
    for rule in RULE_CATALOG:
        if rule.element != "Completeness" or rule.table == "CROSS_TABLE":
            continue
        if "mandatory field mask" not in rule.implementation:
            continue
        fields.setdefault(rule.table, []).append(rule.columns[0])
    return fields


def _conditional_completeness_from_catalog() -> List[Tuple[str, str, str]]:
    conditions: List[Tuple[str, str, str]] = []
    for rule in RULE_CATALOG:
        if (
            rule.element != "Completeness"
            or rule.rule_family != "COMP2"
            or rule.table != "CROSS_TABLE"
            or len(rule.columns) != 2
        ):
            continue
        if "value rule" in rule.implementation:
            continue
        label = rule.condition_label or rule.columns[1]
        conditions.append((rule.columns[0], rule.columns[1], label))
    return conditions


def _value_conditional_completeness_from_catalog() -> List[Tuple]:
    conditions: List[Tuple] = []
    for rule in RULE_CATALOG:
        if (
            rule.element != "Completeness"
            or rule.rule_family != "COMP2"
            or rule.table != "CROSS_TABLE"
            or len(rule.columns) != 2
            or "value rule" not in rule.implementation
        ):
            continue
        label = rule.condition_label or rule.columns[1]
        required_value = _required_value_from_description(rule.description)
        conditions.append((rule.columns[0], None, rule.columns[1], required_value, label))
    return conditions


def _consistency_pairs_from_catalog() -> Dict[str, List[Tuple[str, str]]]:
    pairs: Dict[str, List[Tuple[str, str]]] = {}
    for rule in RULE_CATALOG:
        if rule.element != "Consistency" or len(rule.columns) != 2:
            continue
        if rule.implementation == "deferred":
            continue
        pairs.setdefault(rule.table, []).append((rule.columns[0], rule.columns[1]))
    return pairs


def _validity_regex_from_catalog() -> Dict[str, Dict[str, str]]:
    regex_rules: Dict[str, Dict[str, str]] = {}
    for rule in RULE_CATALOG:
        if rule.element != "Validity" or rule.rule_family == "VALD4":
            continue
        if not rule.regex_pattern:
            continue
        regex_rules.setdefault(rule.table, {})[rule.columns[0]] = rule.regex_pattern
    return regex_rules


def _validity_datetimes_from_catalog() -> Dict[str, List[str]]:
    datetime_rules: Dict[str, List[str]] = {}
    for rule in RULE_CATALOG:
        if rule.element == "Validity" and rule.rule_family == "VALD4":
            datetime_rules.setdefault(rule.table, []).append(rule.columns[0])
    return datetime_rules


def _timeliness_rules_from_catalog() -> Dict[str, List[Dict[str, str]]]:
    timeliness: Dict[str, List[Dict[str, str]]] = {}
    for rule in RULE_CATALOG:
        if (
            rule.element != "Timeliness"
            or rule.rule_family != "TIME1"
            or not rule.index_code
        ):
            continue
        if len(rule.columns) != 2:
            continue
        timeliness.setdefault(rule.table, []).append({
            "start": rule.columns[0],
            "end": rule.columns[1],
            "assign_to": rule.assign_to or rule.columns[1],
        })
    return timeliness


def _uniqueness_keys_from_catalog() -> Dict[str, List[List[str]]]:
    keys: Dict[str, List[List[str]]] = {}
    for rule in RULE_CATALOG:
        if rule.element != "Uniqueness":
            continue
        keys.setdefault(rule.table, []).append(list(rule.columns))
    return keys


