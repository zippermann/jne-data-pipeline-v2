"""
JNE DQ Configuration
====================
Structured governance rule catalog where each static rule is visible as its
own Rule(...) block.

Every rule carries a trailing comment with its Excel index code
(e.g. # CONS2B1, # VALD3B56) so a reader can cross-reference
directly against the JNE Index List.xlsx without searching.

Index code format:  ELEMENT + RULE_TYPE + TABLE_LETTER + FIELD_NUMBER
  Element prefixes: COMP (Completeness)  CONS (Consistency)
                    VALD (Validity)       TIME (Timeliness)
                    UNIQ (Uniqueness)     ACCU (Accuracy)
  Table letters:    A=CMS_APICUST  B=CMS_CNOTE   C=CMS_MRCNOTE
                    D=CMS_DRCNOTE  E=CMS_MHIHOC  F=CMS_DHIHOC
                    G=CMS_DSTATUS  H=CMS_MANIFEST I=CMS_MFCNOTE
                    J=CMS_MFBAG    K=CMS_DMBAG    L=CMS_MMBAG
                    M=CMS_DSMU     N=CMS_MSMU     O=CMS_DRSHEET_PRA
                    P=CMS_DRSHEET  Q=CMS_MRSHEET  R=CMS_CNOTE_POD
                    S=CMS_DHOV_RSHEET              T=CMS_MHOUNDEL_POD
                    U=CMS_DHOUNDEL_POD             V=CMS_MHOCNOTE
                    W=CMS_DHOCNOTE X=CMS_COST_MTRANSIT_AGEN
                    Y=CMS_COST_DTRANSIT_AGEN       Z=CMS_MSJ
                    AA=CMS_DSJ     AB=CMS_MHICNOTE AC=CMS_DHICNOTE
                    AD=CMS_RDSJ    AE=CMS_DBAG_HO

Dynamic manifest configs (OM, IM, HM, TM1..TMN) are built at runtime by
generate_manifest_config() and generate_value_conditionals() below.

This module is standalone: reviewers can open one file and see the rule
catalog. Runtime dictionaries such as COMPLETENESS_FIELDS and VALIDITY_REGEX
are generated from RULE_CATALOG near the bottom of the file.
"""
